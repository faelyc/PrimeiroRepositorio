# 15 Entre com um número e utilize operadores de atribuição para realizar as seguintes
# operações, imprimindo o resultado a cada etapa:

num = 1

num += 5
print("Após adicionar 5:", num)

num *= 2
print("Após multiplicar por 2:", num)

num -= 3
print("Após subtrair 3:", num)

num /= 2
print("Após dividir por 2:", num)

num %= 3
print("Resto da divisão por 3:", num)